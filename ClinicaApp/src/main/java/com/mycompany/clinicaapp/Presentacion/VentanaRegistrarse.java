    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
    String cedula = txtCedula.getText();
    String nombre = txtNombre.getText();
    String contrasena = new String(txtContrasena.getPassword());
    String telefono = txtTelefono.getText();
    int edad = Integer.parseInt(txtEdad.getText());

    //  Validaciones 
    if (cedula.isEmpty() || nombre.isEmpty() || contrasena.isEmpty() || telefono.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Por favor complete todos los campos.", 
            "Campos vac√≠os", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }

    //  Obtener el gestor compartido
    GestorPaciente gestor = GestorPaciente.getInstancia();

    //  Crear paciente
    Paciente nuevo = new Paciente(cedula, nombre, telefono, edad, contrasena);

    //  Registrar
    if (gestor.registrarPaciente(nuevo)) {
        JOptionPane.showMessageDialog(this, 
            "Paciente registrado exitosamente.", 
            "√âxito", 
            JOptionPane.INFORMATION_MESSAGE);
        
        //  Regresar a la ventana de inicio
        VentanaIniciarSesion ventanaInicio = new VentanaIniciarSesion();
        ventanaInicio.setLocationRelativeTo(null);
        ventanaInicio.setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, 
            "Ya existe un paciente con esa c√©dula.", 
            "Duplicado", 
            JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnRegistrarseActionPerformed

