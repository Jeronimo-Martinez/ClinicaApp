    private void btnregistrarpacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarpacienteActionPerformed
        GestorPaciente gestor = GestorPaciente.getInstanciaPaciente();
        String nombre = txtNombre.getText();
        String telefono = txtTelefono.getText();
        String cedula = txtCedula.getText();
        String contrasena = new String(txtContrasena.getPassword());
        Integer edad = Integer.parseInt(txtEdad.getText());
        
        Paciente pacienteNuevo = new Paciente(cedula, nombre, telefono, edad, contrasena);
                
        boolean registrado = gestor.registrarPaciente(pacienteNuevo);
        
        if (registrado) {
        txtNombre.setText("");
        txtTelefono.setText("");
        txtCedula.setText("");
        txtEdad.setText("");
        txtContrasena.setText("");
        }
    
        
    }//GEN-LAST:event_btnregistrarpacienteActionPerformed

