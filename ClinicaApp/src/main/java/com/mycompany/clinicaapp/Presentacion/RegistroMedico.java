    private void btnRegistrarMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarMedicoActionPerformed
        GestorMedico gestorMedico = GestorMedico.getInstanciaMedico();
        String nombre = txtNombre.getText();
        String especialidad = txtEspecialidad.getText() ;
        String cedula = txtCedula.getText();
        String contrasena = new String(txtContrasena.getPassword());
 
        
        Especialidad especialidadMedico = new Especialidad( especialidad);
        Medico medicoNuevo = new Medico(cedula, nombre, especialidadMedico, contrasena);
                
        boolean registrado = gestorMedico.registrarMedico(medicoNuevo);
        
        if (registrado) {
        txtNombre.setText("");
        txtEspecialidad.setText("");
        txtCedula.setText("");
        txtContrasena.setText("");
        }
    }//GEN-LAST:event_btnRegistrarMedicoActionPerformed

